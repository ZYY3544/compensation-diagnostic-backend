"""
JE 职能字典：把 HAY 引擎的 36 个 function 按业务大类分组，方便 HR 在前端下拉选择。

底层 `validation_csv/职能对应岗位特性表.csv` 定义了 36 个 function → Profile 映射。
本文件只做 UI 视角的分组，不修改任何引擎逻辑。
"""

# 大类 → 该大类下的具体职能（保持和 CSV 完全一致，否则查不到 Profile 常模）
FUNCTION_CATALOG: dict[str, list[str]] = {
    '研发与技术': [
        '算法', '软件开发', '硬件开发', '软件测试', '硬件测试',
        '信息安全', '游戏设计', 'IT服务',
    ],
    '产品与设计': [
        '产品管理', '产品运营',
    ],
    '数据与分析': [
        '数据分析与商业智能',
    ],
    '市场与销售': [
        '市场营销', '销售', '销售运营', '电商运营', '广告',
        '媒体推广运营', '客户服务',
    ],
    '运营与供应链': [
        '项目管理', '采购', '供应链管理', '物流',
    ],
    '人力与组织': [
        '人力资源', '行政管理',
    ],
    '财务与法务': [
        '会计', '财务管理', '税务', '内审', '法务',
        '风险管理', '资产管理',
    ],
    '战略与公关': [
        '战略管理', '投融资管理', '公共关系', '网络教育',
    ],
    '其他': [
        '通用职能',
    ],
}


def all_functions() -> list[str]:
    """返回所有可选职能的扁平列表（用于后端校验）。"""
    return [fn for group in FUNCTION_CATALOG.values() for fn in group]


def is_valid_function(name: str) -> bool:
    return name in all_functions()
